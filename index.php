                     $(document).ready(function() {
                        $("#submit").click(function() {

                            $('#nameShow').hide();
                            $('#mailShow').hide();
                            $('#phoneShow').hide();
                            $('#messageShow').hide();
                            $('#hideRect').hide();

                            var name = $("#mesname").val();
                            var email = $("#mesemail").val();
                            var phone = $("#mesphone").val();
                            var message = $("#mestext").val();

                            var fail = "";
                            var failEmail = "";
                            var failPhone = "";
                            var failMessage = "";

                            if (name.length < 3) fail = "Имя не меньше 3 символов";
                            if (email.split('@').length - 1 == 0 || email.split('.').length - 1 == 0) failEmail = "Вы ввели некорректный адрес эл. почты";
                            if (phone.length < 5) failPhone = "Пожалуйста заполните эта поля";
                            if (message.length < 25) failMessage = "Слишком маленькое сообщение";

                            if (fail != "" || failEmail != "" || failPhone != "" || failMessage != "") {
                              $('#nameShow').html(fail + "<p class='clear'></p>");
                              $('#nameShow').show();

                              $('#mailShow').html(failEmail + "<p class='clear'></p>");
                              $('#mailShow').show();

                              $('#phoneShow').html(failPhone + "<p class='clear'></p>");
                              $('#phoneShow').show();

                              $('#messageShow').html(failMessage + "<p class='clear'></p>");
                              $('#messageShow').show();


                              return false;
                            }
                            $.ajax({
                                url: 'ajax/feedback.php',
                                type: 'POST',
                                cache: false,
                                data: {
                                    'name': name,
                                    'email': email,
                                    'phone': phone,
                                    'message': message
                                },
                                dataType: 'html',
                                success: function(data) {
                                    $('#hideRect').html(data + "<p class='clear'></p>");
                                    $('#hideRect').show();
                                    $("#mesname").val('');
                                    $("#mesemail").val('');
                                    $("#mesphone").val('');
                                    $("#mestext").val('');
                                }
                            });
                            return false;
                        });
                    }); </script>﻿

